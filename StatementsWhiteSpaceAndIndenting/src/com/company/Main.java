package com.company;

public class Main {

    public static void main(String[] args) {
        int myVariable = 50;
        myVariable++;
        myVariable--;
        System.out.println("This is a test");

        // Java can carry on statements when the lines break up
        System.out.println("This is" +
                " another" +
                " still more");

        // still a legal statement - but can sometimes make it less readable
        int anotherVariable = 50;
        myVariable--;
        System.out.println("This is another one");
    }
}

